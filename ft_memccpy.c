#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	size_t	i;
	char	*s;
	char	*d;

	i = 0;
	s = (char *)src;
	d = (char *)dest;
	while (s[i] && i < n && s[i] != c)
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
